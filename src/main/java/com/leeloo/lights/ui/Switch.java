package com.leeloo.lights.ui;

public class Switch {

    private String status;
    private int id;

    public Switch() {
    }

    public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
