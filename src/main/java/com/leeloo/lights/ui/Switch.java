package com.leeloo.lights.ui;

public class Switch {

    private String state;
    private int id;

    public Switch() {
    }

    public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
